class Track {
    constructor(id,nombre, duracion, generos){
        this.id = id
        this.nombre =nombre
        this.duracion = duracion
        this.generos = generos
               
    }

    //Getters
    getId(){return this.id}
    getNombre(){return this.nombre}
    getGeneros(){return this.generos}
    getDuracion(){return this.duracion}
    //Getters
      
    
}